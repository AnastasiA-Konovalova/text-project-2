 CREATE TYPE payment_status AS ENUM('Preparing', 'WaitPushTran', 'Authorized', 'PartPaid', 'Funded', 'FullyPaid',
 'Expired', 'Cancelled', 'Rejected', 'Refused', 'Closed', 'Voided', 'Refunded', 'Declined');

    CREATE TABLE IF NOT EXISTS payment_TXPG (
            id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
            payment_order_id BIGINT NOT NULL,
            is_card_saved BOOLEAN DEFAULT FALSE,
            order_id BIGINT,
            sum_of_pay NUMERIC,
            status VARCHAR,
            created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
            updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
    );
