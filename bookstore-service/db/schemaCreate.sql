    CREATE TABLE IF NOT EXISTS book_reviews (
       id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
       book_id BIGINT NOT NULL,
       reviewer_id BIGINT NOT NULL,
       text VARCHAR(255) NOT NULL,
       rating BIGINT NOT NULL,
       created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
       updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
       CONSTRAINT fk_books FOREIGN KEY (book_id) REFERENCES books(id),
       CONSTRAINT fk_users FOREIGN KEY (reviewer_id) REFERENCES users(id)
    );

    CREATE INDEX IF NOT EXISTS idx_book_reviews_book_id ON book_reviews(book_id);

-------------------------------
    CREATE TABLE IF NOT EXISTS favorite_books (
       user_id BIGINT NOT NULL,
       book_id BIGINT NOT NULL,
       CONSTRAINT fk_users FOREIGN KEY (user_id) REFERENCES users(id),
       CONSTRAINT fk_books FOREIGN KEY (book_id) REFERENCES books(id),
       CONSTRAINT pk_favorite_books PRIMARY KEY (user_id, book_id)
    );
