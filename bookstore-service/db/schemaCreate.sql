CREATE TABLE IF NOT EXISTS basket_detail (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    basket_id BIGINT NOT NULL,
    book_id BIGINT NOT NULL,
    price NUMERIC NOT NULL,
    quantity BIGINT NOT NULL,
    CONSTRAINT fk_basket FOREIGN KEY (basket_id) REFERENCES baskets(id),
    CONSTRAINT fk_book FOREIGN KEY (book_id) REFERENCES books(id)
 );

 CREATE INDEX IF NOT EXISTS idx_basket_detail_basket_id ON basket_detail(basket_id);
 CREATE INDEX IF NOT EXISTS idx_basket_detail_book_id ON basket_detail(book_id);
