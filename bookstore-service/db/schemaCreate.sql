 CREATE TABLE IF NOT EXISTS authors (
      id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
      name VARCHAR(255) NOT NULL,
      surname VARCHAR(255) NOT NULL,
      middle_name VARCHAR(255),
      pseudonym VARCHAR(255)
   );

   CREATE INDEX IF NOT EXISTS idx_authors_surname ON authors(surname);
   CREATE INDEX IF NOT EXISTS idx_authors_pseudonym ON authors(pseudonym);

   ------------------------
   CREATE TABLE IF NOT EXISTS publishers (
          id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
          description VARCHAR(255),
          country VARCHAR(255) NOT NULL,
          created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
          updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
       );

       CREATE INDEX IF NOT EXISTS idx_publishers_country ON publishers(country);

   ------------------------
   CREATE TABLE IF NOT EXISTS publisher_series (
          id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
          publisher_id BIGINT NOT NULL,
          name VARCHAR(255) NOT NULL,
          created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
          updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
          CONSTRAINT fk_publishers FOREIGN KEY (publisher_id) REFERENCES publishers(id)
       );

       CREATE INDEX IF NOT EXISTS idx_publisher_series_publisher_id ON publisher_series(publisher_id);
