CREATE TYPE book_genre AS ENUM('HORROR', 'COMEDY', 'ROMANTIC', 'DRAMA', 'THRILLER', 'NON-FICTION');

CREATE TABLE IF NOT EXISTS books (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    title VARCHAR(255) NOT NULL,
    author_id BIGINT NOT NULL,
    genre book_genre NOT NULL,
    publisher_id BIGINT NOT NULL,
    series_id BIGINT,
    price NUMERIC NOT NULL,
    isbn VARCHAR(255),
    release_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    description VARCHAR(255) NOT NULL,
    pages BIGINT NOT NULL,
    weight BIGINT,
    review_count BIGINT NOT NULL,
    average_rating BIGINT NOT NULL,
    CONSTRAINT fk_author FOREIGN KEY (author_id) REFERENCES authors(id),
    CONSTRAINT fk_publisher FOREIGN KEY (publisher_id) REFERENCES publishers(id),
    CONSTRAINT fk_series FOREIGN KEY (series_id) REFERENCES publisher_series(id)
 );

 CREATE INDEX IF NOT EXISTS idx_books_title ON books(title);

